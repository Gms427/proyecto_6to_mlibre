 CREATE TABLE USER(
	ID_USER SERIAL PRIMARY KEY NOT NULL,
	NAME VARCHAR NOT NULL,
	PASSWORD VARCHAR NOT NULL,
	USERNAME VARCHAR NOT NULL,
	EMAIL VARCHAR NOT NULL,
	DATE_OF_BIRTH DATE NOT NULL,
	PHONE VARCHAR NOT NULL,
	STATUS NUMBER NOT NULL,
	IMAGE VARCHAR,
	ROL NUMBER NOT NULL,
	DEPARTAMENT VARCHAR,
	CITY VARCHAR,
	STREET VARCHAR,
	NEIGHBORHOOD VARCHAR
 );
 
 CREATE TABLE STORE(
	ID_USER SERIAL PRIMARY KEY NOT NULL,
	RUT VACHAR NOT NULL,
	SOCIAL_REASON VARCHAR NOT NULL,
	
	FOREIGN KEY(ID_USER) REFERENCES USER(ID_USER)
 );
 
 CREATE TABLE ADMIN(
	ID_USER SERIAL PRIMARY KEY NOT NULL,
	PASSWORD VARCHAR NOT NULL,
	EMAIL VARCHAR NOT NULL
 );
 
 CREATE TABLE PRODUCT(
	ID_PRODUCT VARCHAR NOT NULL,
	PRICE NUMBER NOT NULL,
	F_CURRENCY NOT NULL,
	DESCRIPTION	VARCHAR NOT NULL,
	CATEGORY VARCHAR NOT NULL,
	NAME VARCHAR NOT NULL,
	STOCK NUMBER,
	WARRANTY VARCHAR
 );
 
 CREATE TABLE PRODUCT_COMMENTS(
	ID_COMMENT SERIAL PRIMARY KEY NOT NULL,
	ID_USER	NUMBER NOT NULL,
	COMMENT	VARCHAR NOT NULL,
	ID_PRODUCT	VARCHAR NOT NULL,
	COMMENT_DATE TIMESTAMP,
	
	FOREIGN KEY(ID_USER) REFERENCES USER(ID_USER),
	FOREIGN KEY(ID_PRODUCT) REFERENCES PRODUCT(ID_PRODUCT)
 );
 
 CREATE TABLE PRODUCT_RATING(
	ID_USER	NUMBER PRIMARY KEY NOT NULL,
	ID_PRODUCT VARCHAR PRIMARY KEY NOT NULL,
	TITLE VARCHAR,
	RATING_NUMBER NUMBER,
	DESCRIPTION	VARCHAR,
	
	FOREIGN KEY(ID_USER) REFERENCES USER(ID_USER),
	FOREIGN KEY(ID_PRODUCT) REFERENCES PRODUCT(ID_PRODUCT)
 );
 
 CREATE TABLE FAVOURITE(
	ID_USER	NUMBER PRIMARY KEY NOT NULL,
	ID_PRODUCT	VARCHAR PRIMARY KEY NOT NULL,
	
	FOREIGN KEY(ID_USER) REFERENCES USER(ID_USER),
	FOREIGN KEY(ID_PRODUCT) REFERENCES PRODUCT(ID_PRODUCT)
 );
 
 CREATE TABLE COMMENT_ANSWERS(
	ID_ANSWER SERIAL PRIMARY KEY NOT NULL,
	ID_USER	NUMBER,
	ANSWER	VARCHAR,
	ID_PRODUCT VARCHAR,
	ID_COMMENT VARCHAR,
	ANSWER_DATE TIMESTAMP,
	
	FOREIGN KEY(ID_USER) REFERENCES USER(ID_USER),
	FOREIGN KEY(ID_PRODUCT) REFERENCES PRODUCT(ID_PRODUCT)
 );